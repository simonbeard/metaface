void CTTS_Pron::ProcessTag (CTTS_SMLNode *pNode)
{
	assert (pNode != NULL);

	CTTS_SMLNode *pSubNode = pNode->Children ();
	char *SubstituteText = GetAttribute (pNode, "sub");

	if ((pSubNode != NULL) && (SubstituteText != NULL))
		{	
		pSubNode->SetContent (SubstituteText);
		delete SubstituteText;
		}
}
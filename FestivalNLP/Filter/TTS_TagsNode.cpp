void CTTS_TagsNode::OutputTagNames ()
{
	if (this->IsTextNode())
		{
		cout << this->GetContent() << endl;
		}

	if (Children())
		{
		Children()->OutputTagNames();
		}

	if (Next ())
		{
		Next()->OutputTagNames();
		}
}